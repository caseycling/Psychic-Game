function buildWordDisplay() {
    var returnString = "";
    for(i=0; i<chosenWord.length; i++) { 
        if (chosenWord[i] == " ") {
            returnString += "  ";
        } else if (containsChar(lettersGuessed, chosenWord[i])) {
            returnString += chosenWord[i] + " ";
        } else {
            returnString += "_ ";   
        }
    }
    return returnString;  
}

